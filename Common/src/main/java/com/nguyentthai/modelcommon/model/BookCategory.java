package com.nguyentthai.modelcommon.model;

public class BookCategory {
    long id;
    String name;

    public BookCategory() {
    }

    // Getter and Setter

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    // \Getter and Setter
}
